use std::collections::BTreeMap;
use std::fs;
use std::path::PathBuf;

use anyhow::{Context, Result, bail};
use gazflow_back::gaslib::{load_network, load_scenario_demands};
use gazflow_back::solver::solve_steady_state;

fn main() -> Result<()> {
    let back_root = PathBuf::from(".");
    let network_path = back_root.join("dat/GasLib-11.net");
    let scenario_path = back_root.join("dat/GasLib-11.scn");

    if !network_path.exists() || !scenario_path.exists() {
        bail!(
            "GasLib-11 dataset not found in back/dat (need {:?} and {:?})",
            network_path,
            scenario_path
        );
    }

    let output_path = std::env::var("GAZFLOW_REFERENCE_OUTPUT")
        .map(PathBuf::from)
        .unwrap_or_else(|_| {
            back_root.join("../docs/testing/references/GasLib-11.reference.internal.csv")
        });

    let network = load_network(&network_path).context("load GasLib-11 network")?;
    let scenario = load_scenario_demands(&scenario_path).context("load GasLib-11 scenario")?;
    let result = solve_steady_state(&network, &scenario.demands, 1200, 5e-4)
        .context("solve GasLib-11 steady-state")?;

    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent)
            .with_context(|| format!("create parent directory for {:?}", output_path))?;
    }

    let mut lines = Vec::new();
    lines.push("# Internal reference for regression checks".to_string());
    lines.push("# Generated by: cargo run --bin generate_gaslib11_reference".to_string());
    lines.push("# Format: node,<id>,<pressure_bar>".to_string());

    let mut pressures = BTreeMap::new();
    for (id, p) in result.pressures {
        pressures.insert(id, p);
    }
    for (id, p) in pressures {
        lines.push(format!("node,{id},{p:.10}"));
    }

    fs::write(&output_path, lines.join("\n") + "\n")
        .with_context(|| format!("write {:?}", output_path))?;

    println!("wrote reference file: {}", output_path.display());
    Ok(())
}
